# This learning activity is practice for using casting.

# Challenge 1
my_score = 201
print("The type of my_score is", type(my_score))
print("My score is", my_score)
print("Casting to a string...")
my_score = str(my_score)
print("The type of my_score is now", type(my_score))
print("my_score is", my_score)

